public class variable {
  public static void main(String[] args) {
    int a = 10;
    int b = 20;
    String s = "Hello World";  

    System.out.println(s);
    int d = 66;
    System.out.println(d);

    boolean t = true;
    System.out.println(t);
    double e = 3.14;
    System.out.println(e);

    char f = 'a';
    System.out.println(f);

    int c = a + b;
    System.out.println(c);
  }
}